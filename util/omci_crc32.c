ca_omci_int32_t ca_omci_gen_crc(ca_omci_uint8_t *p, ca_omci_uint32_t len)
{
    ca_omci_int32_t crc = 0xffffffff;

    while (len--) {
        crc = (crc << 8) ^ crctab[(((crc >> 24) ^ *p++) & 0xFF)];
    }

    return (crc ^ 0xffffffff);
}


ca_omci_uint32_t ca_omci_accumulate_crc(
    ca_omci_uint32_t  crc,
    ca_omci_uint8_t  *p,
    ca_omci_uint32_t  len)
{
    crc = crc ^ 0xffffffff;

    while (len--) {
        crc = (crc << 8) ^ crctab[(((crc >> 24) ^ *p++) & 0xFF)];
    }

    return (crc ^ 0xffffffff);
}

ca_omci_uint32_t ca_omci_file_cal_crc32(char *psImgFilePath)
{
    register ca_omci_uint32_t dwCrc = CRC32_INIT_VALUE;
    register ca_omci_uint32_t idx = 0;
    register ca_omci_uint32_t i;
    register ca_omci_uint32_t dwDataLen = 0;
    ca_omci_uint8_t buff[512];
    struct fs_file_t zfp;

    if(NULL == psImgFilePath)
    {
        return 0;
    }

    fs_file_t_init(&zfp);
    if (0 != fs_open(&zfp, psImgFilePath, FS_O_READ))
    {
        OMCICOERR("open %s failed.\n", psImgFilePath);
        return 0;
    }

    while(0 < (dwDataLen =  fs_read(&zfp, buff, 512)))
    {
        for(i=0; i<dwDataLen; i++)
        {
            idx = ((dwCrc >> 24) ^ *(buff + i)) & 0xff;
            dwCrc = (dwCrc << 8) ^ crctab[idx];
        }
    }

    fs_close(&zfp);

    return ~dwCrc;
}

